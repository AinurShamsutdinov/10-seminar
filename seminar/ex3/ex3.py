class Person(object):
    __lastname: str
    __name: str
    __patronymic: str
    __age: int
    __height: int

    def __init__(self, lastname, name, patronymic, age, height):
        self.lastname = lastname
        self.name = name
        self.patronymic = patronymic
        self.age = age
        self.height = height

    def birthday(self):
        self.age += 1

    def full_name(self):
        return str(f'{self.lastname} {self.name} {self.patronymic}')

    def get_age(self):
        return self.age


